// String to work with
const myString = "Jessica Wong";

// 1. Length of the string
console.log("1. Length of string:", myString.length);

// 2. Convert to uppercase
console.log("2. Uppercase:", myString.toUpperCase());

// 3. Convert to lowercase
console.log("3. Lowercase:", myString.toLowerCase());

// 4. Check if the string contains a specific substring
const containss = myString.includes("s");
console.log("4. Contains 's':", containss);

// 5. Extract a portion of the string
const slicedString = myString.slice(0, 4); 

// Extracts characters from index 0 to 4
console.log("5. Sliced String:", slicedString);

